    // ë¡œê·¸ì¸ ì²˜ë¦¬
    async handleLogin() {
        const userId = document.getElementById('userId').value.trim();
        const password = document.getElementById('password').value.trim();
        const errorElement = document.getElementById('loginError');

        if (!userId || !password) {
            this.showError('ì•„ì´ë””ì™€ ë¹„ë°€ë²ˆí˜¸ë¥¼ ì…ë ¥í•´ì£¼ì„¸ìš”.', errorElement);
            return;
        }

        // ì‚¬ìš©ì ë°ì´í„° ë³µí˜¸í™” ë° ê²€ì¦
        const users = await this.decryptUserData();
        if (!users) {
            this.showError('ë³´ì•ˆ ì‹œìŠ¤í…œ ì˜¤ë¥˜ê°€ ë°œìƒí–ˆìŠµë‹ˆë‹¤.', errorElement);
            return;
        }

        // ì‚¬ìš©ì ì¸ì¦
        const user = users.find(u => u.id === userId && u.password === password);
        if (!user) {
            this.showError('ì•„ì´ë”” ë˜ëŠ” ë¹„ë°€ë²ˆí˜¸ê°€ ì¼ì¹˜í•˜ì§€ ì•ŠìŠµë‹ˆë‹¤.', errorElement);
            return;
        }

        // ë¡œê·¸ì¸ ì„±ê³µ
        this.currentUser = user;

        // ì ‘ê·¼ ë¡œê·¸ ì €ì¥
        await this.saveAccessLog();

        // ì‚¬ìš©ìë³„ ì¦ê²¨ì°¾ê¸° ë¡œë“œ
        this.loadUserBookmarks();

        // ì„¸ì…˜ ì €ì¥ (1ì‹œê°„ ìœ íš¨)
        const sessionData = {
            user: user,
            expires: Date.now() + (60 * 60 * 1000)
        };
        localStorage.setItem('militaryLawSession', JSON.stringify(sessionData));

        this.showMainApp();
    }

